package datatypes;

public class Logical {
    public static void main(String[] args) {
        boolean a = false;
        boolean b = false;
        System.out.println(a || b);
        boolean c = false;
        boolean d = false;
        System.out.println(c && d);
        boolean e = false;
        boolean f = true;
        System.out.println();

        // Logical NOT
        a = false;
        System.out.println(!a);
        a = true;
        System.out.println(!a);

    }
}
